package projectSem2;
class nde
{
	int st1,st2;
	double token;
	nde next;
	nde()
	{
		next=null;
	}
	nde(int s1,int s2,double t)
	{
		st1=s1;
		st2=s2;
		token=t;
		next=null;
	}
}
